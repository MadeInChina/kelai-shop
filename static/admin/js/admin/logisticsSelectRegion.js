function updateRegionsAndProvincesCheckState() {
    $('input[type="checkbox"][data-type="bigRegion"]').each(function () {
        var regionId = $(this).attr('id');
        $('input[id^="' + regionId + '-"][data-type="province"]').each(function () {
            var provinceId = $(this).attr('id');
            var provinceCheck = $('input[id^="' + provinceId +'-"][data-type="city"]:checked').length == $('input[id^="' + provinceId +'-"][data-type="city"]').length;
            if (provinceCheck){
                $(this).prop('checked',true);
            } else {
                if ($('input[id^="' + provinceId +'-"][data-type="city"]:checked').length >0){
                    $(this).prop({
                        'indeterminate':true,
                        'checked':false
                    });
                } else {
                    $(this).prop({
                        'indeterminate':false,
                        'checked':false
                    });
                }
            }
        });
        var regionCheck = $('input[id^="' + regionId +'-"][data-type="province"]:checked').length == $('input[id^="' + regionId +'-"][data-type="province"]').length;
        if (regionCheck){
            $(this).prop({
                'checked': true
            });
        } else {
            if ($('input[id^="' + regionId +'-"][data-type="province"]:checked').length >0 || $('input[id^="' + regionId +'-"][data-type="province"]:indeterminate').length >0){
                $(this).prop({
                    'indeterminate':true,
                    'checked':false
                });
            } else {
                $(this).prop({
                    'indeterminate':false,
                    'checked':false
                });
            }
        }
    });
}
