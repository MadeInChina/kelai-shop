function formatSelectedRegions(data) {
    //var data =  $.parseJSON(jsonString);
    var regions = '';
    var ids = '';
    for (var i=0;i<data.length;i++){
        var bigRegionCheckState = getBigRegionCheckState(data[i]);
        if (bigRegionCheckState == 'checked'){
            for (var j=0;j<data[i].provinces.length;j++) {
                regions += data[i].provinces[j].provinceName + ',';
            }
        }else {
            if (bigRegionCheckState == 'indeterminate'){
                for (var j=0;j<data[i].provinces.length;j++) {
                    var provinceCheckState = getProvinceCheckState(data[i].provinces[j]);
                    if(provinceCheckState == 'checked'){
                        regions += data[i].provinces[j].provinceName + ',';
                    }else {
                        if (provinceCheckState == 'indeterminate') {
                            for (var k=0;k<data[i].provinces[j].cities.length;k++) {
                                if (data[i].provinces[j].cities[k].selected) {
                                    regions += data[i].provinces[j].cities[k].cityName + ',';
                                    //ids += data[i].provinces[j].cities[k].cityCode + ',';
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    for (var i=0;i<data.length;i++) {
        for (var j=0;j<data[i].provinces.length;j++) {
            for (var k=0;k<data[i].provinces[j].cities.length;k++) {
                if (data[i].provinces[j].cities[k].selected) {
                    ids += data[i].provinces[j].cities[k].cityCode + ',';
                }
            }
        }
    }
    regions = regions.substr(0,regions.length - 1);
    ids = ids.substr(0,ids.length - 1);
    $('input[name="fields[\'selectedRegionIds\'].value"]').val(ids);
    return regions;
}
function getBigRegionCheckState(bigRegion) {
    var allChecked = true;
    var checkedCount = 0;
    for (var i=0;i<bigRegion.provinces.length;i++){
        for (var j=0;j<bigRegion.provinces[i].cities.length;j++){
            if (bigRegion.provinces[i].cities[j].selected){
                checkedCount++;
            } else {
                allChecked = false;
            }
        }
    }
    if (allChecked){
        return 'checked';
    } else {
        if (checkedCount == 0){
            return 'uncheck';
        } else {
            return 'indeterminate';
        }
    }
}
function getProvinceCheckState(province) {
    var allChecked = true;
    var checkedCount = 0;
    for (var i=0;i<province.cities.length;i++){
        if (province.cities[i].selected){
            checkedCount++;
        } else {
            allChecked = false;
        }
    }
    if (allChecked){
        return 'checked';
    } else {
        if (checkedCount == 0){
            return 'uncheck';
        } else {
            return 'indeterminate';
        }
    }
}